class TestSection7Canary(unittest.TestCase):

    def test_canary_failed_is_an_exception(self):
        from scripts.audit_production import CanaryFailed
        self.assertTrue(issubclass(CanaryFailed, Exception))

    def test_canary_raises_when_credentials_present(self):
        """When companies.count_documents on the
        filing_reps.credentials filter returns > 0, the canary
        section raises CanaryFailed (not warns)."""
        from scripts.audit_production import (
            CanaryFailed, section_filing_reps_canary, ReadOnlyDatabase,
        )
        raw_db = MagicMock()
        raw_db.companies = MagicMock()
        # Pretend 3 docs still carry the legacy field.
        raw_db.companies.count_documents = AsyncMock(return_value=3)
        # Need to also stub aggregate + find for the no-creds branch
        # (won't be reached but the wrapper still inspects
        # __getattr__).
        ro = ReadOnlyDatabase(raw_db)
        # ReadOnlyDatabase wraps via isinstance(AsyncIOMotorCollection)
        # which our MagicMock isn't — so __getattr__ returns the raw
        # mock. Patch the wrapper's instance check to treat any
        # MagicMock as a collection for this test.
        from scripts import audit_production as mod
        with patch.object(mod, "AsyncIOMotorCollection", MagicMock):
            with self.assertRaises(CanaryFailed):
                _run(section_filing_reps_canary(ro))

    def test_canary_clean_when_zero_records(self):
        from scripts.audit_production import (
            section_filing_reps_canary, ReadOnlyDatabase,
        )

        class _AsyncCursor:
            def __init__(self, items):
                self._items = items
            def __aiter__(self):
                async def _gen():
                    for it in self._items:
                        yield it
                return _gen()
            def sort(self, *a, **kw): return self
            def limit(self, n): return self

        raw_db = MagicMock()
        raw_db.companies = MagicMock()
        raw_db.companies.count_documents = AsyncMock(return_value=0)
        raw_db.companies.aggregate = MagicMock(return_value=_AsyncCursor([
            {"_id": "co_a", "name": "Acme", "rep_count": 2},
        ]))
        raw_db.companies.find = MagicMock(return_value=_AsyncCursor([
            {"filing_reps": [{"email": "rep1@example.com"}]},
        ]))
        raw_db.users = MagicMock()
        raw_db.users.find = MagicMock(return_value=_AsyncCursor([]))

        ro = ReadOnlyDatabase(raw_db)
        from scripts import audit_production as mod
        with patch.object(mod, "AsyncIOMotorCollection", MagicMock):
            md, concerns = _run(section_filing_reps_canary(ro))
        self.assertIn("CANARY CLEAN", md)
        # rep1@example.com is a filing_rep without a user account →
        # that's an INFO concern.
        info_concerns = [c for c in concerns if c.severity == "INFO"]
        self.assertEqual(len(info_concerns), 1)


