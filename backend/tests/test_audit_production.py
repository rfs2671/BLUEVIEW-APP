class TestRunAuditCanaryAborts(unittest.TestCase):

    def test_run_audit_returns_1_on_canary_fail(self):
        """When the canary trips during run_audit, the function
        returns exit code 1."""
        from scripts import audit_production as mod
        # Build a mock client + db. We patch the section runner
        # list to call the canary first (which it already does),
        # and the canary's count_documents returns 5.
        raw_db = MagicMock()
        raw_db.companies = MagicMock()
        raw_db.companies.count_documents = AsyncMock(return_value=5)

        class FakeClient:
            def __getitem__(self, name):
                return raw_db

        with patch.object(mod, "AsyncIOMotorClient", lambda *a, **kw: FakeClient()), \
             patch.object(mod, "AsyncIOMotorCollection", MagicMock):
            # Replace SECTION_RUNNERS with just the canary so the
            # other sections don't try to query the mock for shapes
            # we haven't stubbed.
            with patch.object(
                mod, "SECTION_RUNNERS",
                [("filing_reps canary", mod.section_filing_reps_canary)],
            ):
                rc = _run(mod.run_audit("mongodb://x", "smoke_test"))
        self.assertEqual(rc, 1)


