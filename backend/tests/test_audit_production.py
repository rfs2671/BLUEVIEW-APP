class TestPythonPresetDetectionMatchesJS(unittest.TestCase):
    """Loads frontend/src/utils/notificationPresets.js source, extracts
    the constants via regex (same approach as
    test_notification_presets_shape.py), verifies the Python
    constants are byte-equal."""

    PRESETS_FILE = (
        _FRONTEND / "src" / "utils" / "notificationPresets.js"
    )

    def _extract_kind_list(self, source: str, marker: str):
        pattern = (
            rf"export const {re.escape(marker)} = \[(.*?)\];"
        )
        match = re.search(pattern, source, re.DOTALL)
        if not match:
            return []
        body = match.group(1)
        body = re.sub(r"//[^\n]*", "", body)
        return re.findall(r"'([^']+)'", body)

    def test_python_all_kinds_matches_js(self):
        from scripts.audit_production import ALL_KINDS
        text = self.PRESETS_FILE.read_text(encoding="utf-8")
        js_kinds = self._extract_kind_list(text, "ALL_KINDS")
        self.assertEqual(
            sorted(ALL_KINDS),
            sorted(js_kinds),
            "Python ALL_KINDS must match JS notificationPresets.js",
        )

    def test_python_critical_email_kinds_matches_js(self):
        from scripts.audit_production import CRITICAL_EMAIL_KINDS
        text = self.PRESETS_FILE.read_text(encoding="utf-8")
        js_kinds = self._extract_kind_list(text, "CRITICAL_EMAIL_KINDS")
        self.assertEqual(
            sorted(CRITICAL_EMAIL_KINDS),
            sorted(js_kinds),
            "Python CRITICAL_EMAIL_KINDS must match JS",
        )

    def test_python_standard_digest_kinds_matches_js(self):
        from scripts.audit_production import STANDARD_DIGEST_KINDS
        text = self.PRESETS_FILE.read_text(encoding="utf-8")
        js_kinds = self._extract_kind_list(text, "STANDARD_DIGEST_KINDS")
        self.assertEqual(
            sorted(STANDARD_DIGEST_KINDS),
            sorted(js_kinds),
            "Python STANDARD_DIGEST_KINDS must match JS",
        )

    def test_python_preset_routes_match_js_literals(self):
        """The JS source has three literal channel_routes_default
        objects, one per preset. Verify Python's
        build_preset_channel_routes returns the same shapes."""
        from scripts.audit_production import build_preset_channel_routes
        text = self.PRESETS_FILE.read_text(encoding="utf-8")
        # Critical only: { critical: ['email'], warning: [], info: [] }
        self.assertIn(
            "{ critical: ['email'], warning: [], info: [] }",
            text,
        )
        self.assertEqual(
            build_preset_channel_routes("critical_only"),
            {"critical": ["email"], "warning": [], "info": []},
        )
        # Standard: { critical: ['email'], warning: ['email'], info: [] }
        self.assertIn(
            "{ critical: ['email'], warning: ['email'], info: [] }",
            text,
        )
        self.assertEqual(
            build_preset_channel_routes("standard"),
            {"critical": ["email"], "warning": ["email"], "info": []},
        )
        # Everything: { critical: ['email'], warning: ['email'], info: ['email'] }
        self.assertIn(
            "{ critical: ['email'], warning: ['email'], info: ['email'] }",
            text,
        )
        self.assertEqual(
            build_preset_channel_routes("everything"),
            {"critical": ["email"], "warning": ["email"], "info": ["email"]},
        )


