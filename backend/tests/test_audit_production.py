# ── ReadOnlyDatabase guard ────────────────────────────────────────


class TestReadOnlyGuard(unittest.TestCase):

    def _wrapped_collection(self):
        from scripts.audit_production import ReadOnlyCollection
        # Build a mock that pretends to be a Motor collection. The
        # name attribute is what the wrapper surfaces in error
        # messages.
        real = MagicMock()
        real.name = "test_coll"
        # Attach reasonable attributes for read methods.
        real.find = MagicMock()
        real.count_documents = AsyncMock(return_value=42)
        real.aggregate = MagicMock()
        real.find_one = AsyncMock(return_value=None)
        real.index_information = AsyncMock(return_value={})
        return ReadOnlyCollection(real), real

    def test_insert_one_raises(self):
        wrapped, _ = self._wrapped_collection()
        with self.assertRaises(RuntimeError) as cm:
            _ = wrapped.insert_one  # access alone triggers the guard
        self.assertIn("read-only", str(cm.exception).lower())
        self.assertIn("insert_one", str(cm.exception))

    def test_every_banned_method_raises(self):
        from scripts.audit_production import _BANNED_COLLECTION_METHODS
        wrapped, _ = self._wrapped_collection()
        for method in _BANNED_COLLECTION_METHODS:
            with self.assertRaises(RuntimeError, msg=method):
                _ = getattr(wrapped, method)

    def test_read_methods_pass_through(self):
        wrapped, real = self._wrapped_collection()
        # find / count_documents / aggregate / find_one / index_information
        # all pass through to the underlying collection.
        self.assertIs(wrapped.find, real.find)
        self.assertIs(wrapped.count_documents, real.count_documents)
        self.assertIs(wrapped.aggregate, real.aggregate)
        self.assertIs(wrapped.find_one, real.find_one)
        self.assertIs(wrapped.index_information, real.index_information)

    def test_collection_name_property_passes_through(self):
        wrapped, _ = self._wrapped_collection()
        self.assertEqual(wrapped.name, "test_coll")


